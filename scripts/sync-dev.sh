#!/bin/bash

echo "=== Синхронизация разработки ==="

# Проверяем статус git
echo "Проверяем статус git..."
git status --porcelain

# Если есть изменения, коммитим их
if [ -n "$(git status --porcelain)" ]; then
    echo "Обнаружены изменения, коммитим..."
    git add .
    git commit -m "[DEV] Автоматический коммит изменений разработки"
    echo "Изменения закоммичены"
else
    echo "Изменений не обнаружено"
fi

# Показываем последние коммиты
echo "Последние коммиты:"
git log --oneline -5

echo "=== Синхронизация завершена ==="
