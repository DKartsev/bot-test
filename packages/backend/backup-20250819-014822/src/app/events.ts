import { EventEmitter } from "events";

/**
 * Класс EventBus предоставляет простую шину событий в памяти.
 * Он используется для отправки уведомлений в реальном времени между
 * различными частями приложения (например, от админ-ручки к WebSocket-потоку).
 */
export class EventBus extends EventEmitter {}

// Для простоты мы будем использовать один инстанс на все приложение,
// но создание класса позволяет легко заменить его на более сложную
// реализацию в будущем (например, на основе Redis Pub/Sub).
export const appEventBus = new EventBus();
